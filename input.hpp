int MapKey (int key)
{
	int result;
	int modified = ( key >> 16 ) & 255;
	bool is_extended = false;

	if ( modified > 127)
		return 0;

	if ( key & ( 1 << 24 ) )
		is_extended = true;

	result = scantokey[modified];

	if ( !is_extended )
	{
		switch ( result )
		{
		case K_HOME:
			return K_KP_HOME;
		case K_UPARROW:
			return K_KP_UPARROW;
		case K_PGUP:
			return K_KP_PGUP;
		case K_LEFTARROW:
			return K_KP_LEFTARROW;
		case K_RIGHTARROW:
			return K_KP_RIGHTARROW;
		case K_END:
			return K_KP_END;
		case K_DOWNARROW:
			return K_KP_DOWNARROW;
		case K_PGDN:
			return K_KP_PGDN;
		case K_INS:
			return K_KP_INS;
		case K_DEL:
			return K_KP_DEL;
		default:
			return result;
		}
	}
	else
	{
		switch ( result )
		{
		case 0x0D:
			return K_KP_ENTER;
		case 0x2F:
			return K_KP_SLASH;
		case 0xAF:
			return K_KP_PLUS;
		}
		return result;
	}
}


int keyRepeats[256] = {0};

void KeyEvent(int key, bool down)
{
    if (down)
    {
        keyRepeats[MapKey(key)]++;
        return;
    }
    keyRepeats[MapKey(key)]=0;

}


void GetInput(void)
{

    //-------------------------
    // KeyBoard
    //-------------------------
    for (int i=0; i<256; ++i)
    {
        //if (keyRepeats[i]!=0) printf("KEY %i", i);
    }
    Forward = 0; Backward = 0; LeftStrafe=0; RightStrafe=0;
    if (keyRepeats[119]) Forward=1;
    if (keyRepeats[115]) Backward=1;
    if (keyRepeats[97])  LeftStrafe=1;
    if (keyRepeats[100]) RightStrafe=1;
    
    //-------------------------
    // Mouse position
    //-------------------------
    POINT		current_pos;

	// find mouse movement
	if (!GetCursorPos (&current_pos))
		return;
    
    int mx,my;
	mx = current_pos.x-X_CENTER;
	my = current_pos.y-Y_CENTER;
    
    mouseX = mx;
    mouseY = my;

    if (mx || my)
        SetCursorPos (X_CENTER, Y_CENTER);

}

